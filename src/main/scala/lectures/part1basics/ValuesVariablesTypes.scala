package lectures.part1basics

object ValuesVariablesTypes extends App {
    // VALs are immutable
    // semicolons are unnecessary and only used if multiple expression on the same line
    // compiler can ingfer types
    val x: Int = 45
    val aString = "hellowww"
    val aBoolean = true
    val aChar = 'c'
    val aShort = 345
    val aLong = 13423534643634L
    val aFloat = 2.0f
    val aDouble = 56.234523

    // VARs are mutable
    var aInt = 454
    aInt = 56
}
